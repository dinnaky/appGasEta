package devandroid.arthursilvio.appgaseta.model;

public class Fuel {
    private String nameFuel;
    private Number priceFuel;
    private String Recommendation;

    public String getNameFuel() {
        return nameFuel;
    }

    public void setNameFuel(String nameFuel) {
        this.nameFuel = nameFuel;
    }

    public Number getPriceFuel() {
        return priceFuel;
    }

    public void setPriceFuel(Number priceFuel) {
        this.priceFuel = priceFuel;
    }

    public String getRecommendation() {
        return Recommendation;
    }

    public void setRecommendation(String recommendation) {
        Recommendation = recommendation;
    }
}
